#include<iostream>
#include<string>
using namespace std;
int main(){
//比较运算符
	int a = 10;
	int b = 20;
//相等
	cout << (a == b) <<endl;
//不等
	cout << (a != b) <<endl;
//大于
	cout << (a > b) <<endl;
//小于
	cout << (a < b) <<endl;
//大于等于
	cout << (a >= b) <<endl;
//小于等于
	cout << (a <= b) <<endl;

return 0;
}
