#include<iostream>
#include<string>
using namespace std;
int main(){

//赋值预算符
//1.	=
	int a = 10;
	a = 100;
	cout << "a=" << a << endl;
//2.	+=
	a = 10;
	a += 2;//a=a+2
	cout << "a=" << a << endl;
//3.	-=
	a = 10;
	a -= 2;
	cout << "a=" << a << endl;
//4.	*=
	a = 10;
	a *= 2;
	cout << "a=" << a << endl;
//5.	/=
	a = 10;
	a /= 2;
	cout << "a=" << a << endl;
//6.	%=
	a = 10;
	a %= 2;
	cout << "a=" << a << endl;


return 0;
}
